    private void cargarHistorial() {
        try (FileReader fr = new FileReader("bitacoraCalculadora.txt");
             BufferedReader br = new BufferedReader(fr)) {
            String linea;
            StringBuilder historial = new StringBuilder();
            while ((linea = br.readLine()) != null) {
                historial.append(linea).append("\n");
            }
            jTextAreaHistorial.setText(historial.toString());
        } catch (IOException e) {
            jTextAreaHistorial.setText("No se pudo cargar el historial: " + e.getMessage());
        }
    }

